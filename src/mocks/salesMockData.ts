let nextOrderId = 11;

export const addSalesOrder = (
  order: Omit<SalesOrder, "id" | "orderNumber" | "createdAt" | "updatedAt">,
): SalesOrder => {
  const now = new Date().toISOString();
  const newOrder: SalesOrder = {
    ...order,
    id: nextOrderId++,
    orderNumber: `SO-2024-${String(nextOrderId - 1).padStart(4, "0")}`,
    createdAt: now,
    updatedAt: now,
  };
  mockSalesOrders.unshift(newOrder);
  return newOrder;
};

export const updateSalesOrder = (
  id: number,
  updates: Partial<SalesOrder>,
): SalesOrder | null => {
  const index = mockSalesOrders.findIndex((o) => o.id === id);
  if (index === -1) return null;

  mockSalesOrders[index] = {
    ...mockSalesOrders[index],
    ...updates,
    updatedAt: new Date().toISOString(),
  };
  return mockSalesOrders[index];
};

export const deleteSalesOrder = (id: number): boolean => {
  const index = mockSalesOrders.findIndex((o) => o.id === id);
  if (index === -1) return false;
  mockSalesOrders.splice(index, 1);
  return true;
};
