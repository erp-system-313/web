let nextInvoiceId = 9;

export const addInvoice = (
  invoice: Omit<Invoice, "id" | "invoiceNumber" | "createdAt" | "updatedAt">,
): Invoice => {
  const now = new Date().toISOString();
  const newInvoice: Invoice = {
    ...invoice,
    id: nextInvoiceId++,
    invoiceNumber: `INV-2024-${String(nextInvoiceId - 1).padStart(4, "0")}`,
    createdAt: now,
    updatedAt: now,
  };
  mockInvoices.unshift(newInvoice);
  return newInvoice;
};

export const updateInvoice = (
  id: number,
  updates: Partial<Invoice>,
): Invoice | null => {
  const index = mockInvoices.findIndex((i) => i.id === id);
  if (index === -1) return null;
  mockInvoices[index] = {
    ...mockInvoices[index],
    ...updates,
    updatedAt: new Date().toISOString(),
  };
  return mockInvoices[index];
};

export const recordPayment = (
  payment: Omit<Payment, "id" | "createdAt">,
): Payment => {
  const newPayment: Payment = {
    ...payment,
    id: mockPayments.length + 1,
    createdAt: new Date().toISOString(),
  };
  mockPayments.push(newPayment);

  const invoice = mockInvoices.find((i) => i.id === payment.invoiceId);
  if (invoice) {
    invoice.paidAmount += payment.amount;
    invoice.balanceDue = invoice.total - invoice.paidAmount;
    if (invoice.balanceDue <= 0) {
      invoice.status = "PAID";
      invoice.balanceDue = 0;
    }
    invoice.updatedAt = new Date().toISOString();
    if (!invoice.payments) invoice.payments = [];
    invoice.payments.push(newPayment);
  }

  return newPayment;
};
